					return true;
			}
		}
		
		function validateDes(){

		//if it's NOT valid
			if($("#message").val().length < 3 || $("#message").val() == 'Your Message')  {
			$('#messageInfo').text("Please enter message");
			return false;
			}
			//if it's valid
